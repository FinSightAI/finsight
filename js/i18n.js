    /**
     * Initialize i18n with saved language
     */
    init() {
        // Priority: URL ?lang param → shared wl_lang key → app-specific setting → browser
        const urlLang = new URLSearchParams(window.location.search).get('lang');
        const wlLang  = localStorage.getItem('wl_lang');
        const settings = Storage.getSettings();
        const resolved = urlLang || wlLang || settings.language;
        if (resolved && this.languages.includes(resolved)) {
            this.currentLanguage = resolved;
        } else {
            const bl = (navigator.language || 'en').toLowerCase();
            // Hebrew not auto-detected — user selects manually
            if (bl.startsWith('pt')) this.currentLanguage = 'pt';
            else if (bl.startsWith('es')) this.currentLanguage = 'es';
            else this.currentLanguage = 'en';
        }
        // Persist resolved language to both keys
        localStorage.setItem('wl_lang', this.currentLanguage);
        Storage.saveSettings({ ...settings, language: this.currentLanguage });
        this.updatePageDirection();
        this.translatePage();
    },

    /**
     * Get translation by key path
     * @param {string} path - Dot notation path (e.g., 'nav.dashboard')
     * @returns {string} Translated text
     */
    t(path) {
        const keys = path.split('.');
        let result = this.translations[this.currentLanguage];
        for (const key of keys) {
            result = result?.[key];
        }
        return result || path;
    },

    /**
     * Available languages
     */
    languages: ['he', 'en', 'pt', 'es'],
    languageNames: { he: 'עברית', en: 'English', pt: 'Português', es: 'Español' },

    /**
     * Toggle between languages (Hebrew -> English -> Portuguese -> Hebrew)
     */
    toggleLanguage() {
        var currentIdx = this.languages.indexOf(this.currentLanguage);
        var nextIdx = (currentIdx + 1) % this.languages.length;
        this.currentLanguage = this.languages[nextIdx];
        Storage.saveSettings({ ...Storage.getSettings(), language: this.currentLanguage });
        this.updatePageDirection();
        this.translatePage();
    },

    /**
     * Set specific language
     * @param {string} lang - 'he', 'en', or 'pt'
     */
    setLanguage(lang) {
        if (this.languages.includes(lang)) {
            this.currentLanguage = lang;
            localStorage.setItem('wl_lang', lang);  // shared cross-app key
            Storage.saveSettings({ ...Storage.getSettings(), language: lang });
            this.updatePageDirection();
            this.translatePage();
        }
    },

    /**
     * Update page direction based on language
     */
    updatePageDirection() {
        document.documentElement.lang = this.currentLanguage;
        document.documentElement.dir = this.currentLanguage === 'he' ? 'rtl' : 'ltr';
        document.body.classList.toggle('ltr', this.currentLanguage !== 'he');
    },

    /**
     * Translate all elements with data-i18n attribute
     */
    translatePage() {
        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            el.textContent = this.t(key);
        });
        document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
            const key = el.getAttribute('data-i18n-placeholder');
            el.placeholder = this.t(key);
        });
        document.querySelectorAll('[data-i18n-title]').forEach(el => {
            const key = el.getAttribute('data-i18n-title');
            el.title = this.t(key);
        });
        // Update language switcher buttons - highlight active language
        document.querySelectorAll('.lang-btn').forEach(btn => {
            const lang = btn.getAttribute('data-lang');
            btn.classList.toggle('active', lang === this.currentLanguage);
        });
    },

    /**
     * Get locale code for Intl formatting
     */
    getLocale() {
        const locales = { he: 'he-IL', en: 'en-US', pt: 'pt-BR', es: 'es-419' };
        return locales[this.currentLanguage] || 'en-US';
    },

    /**
     * Format currency
     * @param {number} amount - Amount to format
     * @param {string} currency - Currency code (default: ILS)
     * @returns {string} Formatted currency string
     */
    getCurrency() {
        return this.t('common.currencyCode') || 'ILS';
    },

    formatCurrency(amount, currency) {
        const cur = currency || this.getCurrency();
        const formatter = new Intl.NumberFormat(this.getLocale(), {
            style: 'currency',
            currency: cur,
            minimumFractionDigits: 0,
            maximumFractionDigits: 0
        });
        return formatter.format(amount);
    },

    /**
     * Format number with locale
     * @param {number} num - Number to format
     * @returns {string} Formatted number
     */
    formatNumber(num) {
        return new Intl.NumberFormat(this.getLocale()).format(num);
    },

    /**
     * Format date
     * @param {string|Date} date - Date to format
     * @returns {string} Formatted date
     */
    formatDate(date) {
        const d = new Date(date);
        return new Intl.DateTimeFormat(this.getLocale()).format(d);
    },

    /**
     * Get current month name
     * @returns {string} Month name
     */
    getCurrentMonthName() {
        return this.translations[this.currentLanguage].months[new Date().getMonth()];
    }
